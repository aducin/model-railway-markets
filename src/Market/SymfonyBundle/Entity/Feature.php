{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var \Market\SymfonyBundle\Entity\Product
     */
    private $product;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set product
     *
     * @param \Market\SymfonyBundle\Entity\Product $product
     *
     * @return Feature
     */
    public function setProduct(\Market\SymfonyBundle\Entity\Product $product = null)
    {
        $this->product = $product;

        return $this;
    }

    /**
     * Get product
     *
     * @return \Market\SymfonyBundle\Entity\Product
     */
    public function getProduct()
    {
        return $this->product;
    }
}
